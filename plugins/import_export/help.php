<h4>XOutputFilter-Plugin import_export</h4>

<p>
Mit diesem Plugin können die XOutputFilter-Ersetzungen komplett als SQL-Dump exportiert/importiert werden,
oder einzelne Ersetzungstypen (z.B. Abkürzungen, Frontend ...) als CSV-Datei exportiert/importiert werden.
</p>
<p>
Die Backups werden im Unterverzeichnis <strong>backup</strong> des Plugins <strong>import_export</strong> gespeichert und können direkt importiert oder als Datei heruntergeladen werden.
</p>
<p>
<strong>Hinweis:</strong><br>
Die CSV-Backups haben ein "eigenes" Format da hier z.B. Newlines für einen korrekten Import umgesetzt werden!
</p>
