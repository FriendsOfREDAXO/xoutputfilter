<h4>XOutputFilter-Plugin abbrev - Abkürzungen</h4>

<p>Mit diesem Plugin können auf der Webseite Abkürzungen und Akronyme mit einem Hinweistext (Title) versehen werden.</p>

<p>Die Abkürzungen werden in den &lt;abbr&gt;-Tag eingebettet. Dieser Filter wird nur innerhalb des &lt;body&gt; angewendet.</p>

<p>
<a href="https://de.wikipedia.org/wiki/Abkürzung">https://de.wikipedia.org/wiki/Abkürzung</a><br>
<a href="https://de.wikipedia.org/wiki/Akronym">https://de.wikipedia.org/wiki/Akronym</a><br>
<a href="https://de.wikipedia.org/wiki/Liste_von_Abk%C3%BCrzungen_(Computer)">https://de.wikipedia.org/wiki/Liste_von_Abk%C3%BCrzungen_(Computer)</a>
</p>

<p>Beispiel:</p>

<p>
Vorher:
<code>Abk.</code>
</p>

<p>
Nachher:
<code>&lt;abbr title="Abkürzung"&gt;Abk.&lt;/abbr&gt;</code>
</p>
