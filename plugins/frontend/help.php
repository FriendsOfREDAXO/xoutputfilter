<h4>XOutputFilter-Plugin frontend - Frontend-Ersetzungen</h4>

<p>Mit diesem Plugin k&ouml;nnen im Frontend Marker durch verschiedene Ersetzungsmöglichkeiten ersetzt werden.</p>

<p>Die angelegten Ersetzungen werden sortiert nach Namen mit nur einem Aufruf des Extensionpoints OUTPUT_FILTER verarbeitet.</p>

